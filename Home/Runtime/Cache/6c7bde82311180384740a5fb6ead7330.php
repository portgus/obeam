         <h1 class="new-tit">装修瓷砖有哪些分类,以后别再分错了</h1>
         <div class="h15"></div>
          <div class="new-liu">发布日期 : 2018-07-06&nbsp;&nbsp;&nbsp;&nbsp;5人浏览</div>
          <div class="h20"></div>
          <div class="new_show_tp">
            <img src="/uploadfile/image/20180706/20180706182630_57529.png" alt="装修瓷砖有哪些分类,以后别再分错了" title="装修瓷砖有哪些分类,以后别再分错了">
          </div>
          <div class="h20"></div>
          <div class="xinxian-con">
                           <p style="font-size:14px;color:#4c4c4c;line-height: 25px;"><p>
	有时候我们分不清这些材料的分类，就是因为没有在同一个层级上比较，就会觉得分类繁杂，但事实上，分门别类地分类很快就能分清楚了。就比如说装修瓷砖有哪些分类，按照不同的分类标准有不同的分法，本文我们一起来看一下，以后就别分错了。
</p>
<p>
	1.按用途可分为外墙砖、内墙砖、地砖、广场砖、工业砖。
</p>
<p>
	这个分类就很清楚了，按照使用的地方不同，外墙贴的就叫外墙砖，以此类推。
</p>
<p>
	2.按成型可分为干压成型砖、挤压成型砖、可塑成型砖。
</p>
<p>
	按瓷砖的成型方式，分为以上几种，我们在选购瓷砖时，可以忽略这个分类，只了解一下即可。
</p>
<p>
	干压成型就是将干粉坯料填充入金属模腔中，施以压力使其成为致密坯体。
</p>
挤压成型，是指加工过程是坯料在三向不均匀压应力作用下，从模具的孔口或缝隙挤出使之横截面积减小长度增加，然后成为所需制品。<br />
<p>
	可塑成型，是指所用坯料一般加入一定量的水分(在16%左右)，将预制好的坯料投入挤泥机中挤成泥条，然后切割成荒坯，压制成型。
</p>
<p>
	3.按烧成可分为氧化性瓷砖、还原性瓷砖。
</p>
<p>
	化学学得好的就可以明白这个分类了，有机物反应时把有机物引入氧或脱去氢的作用叫氧化，引入氢或失去氧的作用叫还原。这是烧成时的化学反应，对于普通的建材选购者，这个也不必特别了解。
</p>
<p>
	4.按施釉分：有釉砖、无釉砖。
</p>
<p>
	釉是覆盖在陶瓷制品表面的无色或有色的玻璃质薄层，能增加瓷砖的稳定性、美观性，还易擦洗。
</p>
<p>
	5.按生产工艺可分为釉面砖、通体砖、抛光砖、玻化砖、马赛克。
</p>
<p>
	通体砖：是将岩石碎屑经过高压压制后再烧制而成，表面抛光后坚硬度可与石材相比，吸水率更低，耐磨性好。通体砖的表面不上釉，而且正面和反面的材质和色泽一致，因此得名。其花色比不上釉面砖。由于其表面较为粗糙，防滑性能较好，市售多数的防滑砖属于通体砖，广泛用于外墙、过道、室外走道等地方。
</p>
<p>
	<br />
</p>
<p>
	<img src="../uploadfile/image/20180706/20180706182623_58427.png" alt="装修瓷砖有哪些分类,以后别再分错了" title="装修瓷砖有哪些分类,以后别再分错了" width="550" height="390" align="" />
</p>
<p>
	<br />
</p>
<p>
	抛光砖：是通体砖坯体的表面经过打磨而成的一种光亮的砖，属通体砖的一种。相对通体砖而言，抛光砖表面要光洁得多。
</p>
<p>
	<br />
</p>
<p>
	<img src="../uploadfile/image/20180706/20180706182554_28712.png" alt="装修瓷砖有哪些分类,以后别再分错了" title="装修瓷砖有哪些分类,以后别再分错了" width="500" height="415" align="" />
</p>
<p>
	<br />
</p>
<p>
	玻化砖：是瓷质抛光砖的俗称，是通体砖坯体的表面经过打磨而成的一种光亮的砖，属通体砖的一种。那玻化砖和抛光砖有什么区别呢？吸水率低于0.5%的陶瓷砖都称为玻化砖，高于0.5%就只能是抛光砖，然后将玻化砖进行镜面抛光即得玻化抛光砖，因为吸水率低的缘故其硬度也相对比较高，不容易有划痕，所以主要应用于公共空间的装饰，如家中的客厅、办公室、店铺等。
</p>
<p>
	<br />
</p>
<p>
	<img src="../uploadfile/image/20180706/20180706182603_74033.png" alt="装修瓷砖有哪些分类,以后别再分错了" title="装修瓷砖有哪些分类,以后别再分错了" width="550" height="357" align="" />
</p>
<p>
	<br />
</p>
<p>
	釉面砖：釉面砖是砖的表面经过施釉高温高压烧制处理的瓷砖，釉面砖表面可以做各种图案和花纹，比抛光砖色彩和图案丰富，因为表面是釉料，所以耐磨性不如抛光砖。
</p>
<p>
	<br />
</p>
<p>
	<img src="../uploadfile/image/20180706/20180706182612_90779.jpg" alt="装修瓷砖有哪些分类,以后别再分错了" title="装修瓷砖有哪些分类,以后别再分错了" width="550" height="367" align="" />
</p>
<p>
	<br />
</p>
<p>
	马赛克：又称锦砖或纸皮砖，用于拼成各种装饰图案用的片状小瓷砖。坯料经半干压成形，窑内焙烧成锦砖。其吸水率不大于不大于0.2%，常用在卫生间、游泳馆等。
</p>
<p>
	<br />
</p>
